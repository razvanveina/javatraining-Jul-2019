package com.savnet.session01;

public class PrimitiveTypes {
	public static void main(String[] args) {

		// tipuri numerice intregi
		byte b = 10; // 8 biti 2^8 = 256
		short sh = 25; // 16
		int n = 10; // 32
		long lg = 100; // 64

		// tipuri numerice reale
		float f; // 32 biti
		double d; // 64 biti

		// tipul caracter
		char ch;

		// tipul boolean
		boolean bool;
	}
}
